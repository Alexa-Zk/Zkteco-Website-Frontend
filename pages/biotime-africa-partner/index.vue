import AboutMission from '~/components/partials/page/website/AboutMission';
import AboutVision from '~/components/partials/page/website/AboutVision';
import AboutValue from '~/components/partials/page/website/AboutValue';
import Enquiry from '~/components/partials/biotime-ng/Enquiry';

export default {
    head() {
        return {
            titleTemplate: 'Biotime Africa Partner',
            meta: [
                {
                    hid: 'description',
                    name: 'description',
                    content: 'About ZKTeco West Africa'
                }
            ]
        };
    },
    mixins: [validationMixin],
    components: {
        AboutMission,
        AboutVision,
        AboutValue,
        BreadCrumb,
        Enquiry
    },

    layout: 'layout-default-website',
    transition: 'zoom',

    data: () => {
        return {
            showError: false,
            showSuccess: false,
            loading: '',
            fullname: '',
            emailAddress: '',
            phone: '',
            country: '',
            state: '',
            qualification: '',
            errors: '',
            snackBarMessage:
                'Form Submitted Successfully. You will be contacted by one of our customer representatives.',
            snackbar: false,
            breadCrumb: [
                {
                    text: 'Home',
                    url: '/'
                },
                {
                    text: 'About Us'
                }
            ]
        };
    },
    validations: {
        fullname: { required },
        emailAddress: { required, email },
        phone: { required, numeric },
        country: { required },
        state: { required },
        qualification: { required }
    },
    methods: {
        resetForm() {
            this.fullname = '';
            this.emailAddress = '';
            this.phone = '';
            this.country = '';
            this.state = '';
            this.qualification = '';
        },
        async bookAppointment() {
            this.$v.$touch();
            if (this.$v.$invalid) {
                return false;
            } else if (this.$v.$error) {
                return false;
            } else {
                this.loading = true;
                const payload = {
                    name: this.fullname,
                    email: this.emailAddress,
                    phone_number: this.phone,
                    country: this.country,
                    state: this.state,
                    qualification: this.qualification
                };

                const response = await this.$store.dispatch(
                    'website/bookAppointment',
                    payload
                );

                if (response) {
                    this.loading = false;
                    this.snackbar = true;
                    this.showError = false;
                    setTimeout(() => {
                        window.scrollTo({ top: 0, behavior: 'smooth' });
                        this.resetForm();
                        //window.location.reload();
                        //this.$router.push('/biotime-ng');
                    }, 3001);
                } else {
                    this.loading = false;
                    this.showSuccess = false;
                    this.showError = true;
                }
            }
        }
    }
};
</script>

